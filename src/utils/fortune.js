// 생년월일 + 오늘 날짜 기반 결정론적 운세 계산

function hash(str) {
  let h = 5381
  for (let i = 0; i < str.length; i++) {
    h = ((h << 5) + h) ^ str.charCodeAt(i)
    h = h >>> 0
  }
  return h
}

function seededRandom(seed, index) {
  const h = hash(String(seed) + String(index))
  return (h % 1000) / 1000
}

