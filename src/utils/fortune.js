const LUCKY_ITEMS = ['거울', '책', '편지', '꽃', '반지', '동전', '열쇠', '수정', '향초', '리본']
const LUCKY_TIMES = ['오전 7시', '오전 10시', '오후 1시', '오후 3시', '오후 6시', '오후 9시']
const LUCKY_DIRECTIONS = ['동쪽', '서쪽', '남쪽', '북쪽', '동남쪽', '동북쪽']

export function getTodayFortune(birthdate) {
  const today = new Date().toISOString().split('T')[0]
  const seed = hash(birthdate + today)

  const d = new Date(birthdate)
  const month = d.getMonth() + 1
  const day = d.getDate()
  const sign = getStarSign(month, day)
  const elementData = ELEMENT_FORTUNE[sign.element]

  function pick(arr, offset) {
    return arr[Math.floor(seededRandom(seed, offset) * arr.length)]
  }

  function pickScore(offset) {
    const r = seededRandom(seed, offset)
    if (r < 0.10) return 0
    if (r < 0.30) return 1
    if (r < 0.60) return 2
    if (r < 0.85) return 3
    return 4
  }

  function pickFortune(category, scoreOffset, variantOffset) {
    const score = pickScore(scoreOffset)
    const variants = elementData[category][score]
    const idx = Math.floor(seededRandom(seed, variantOffset) * variants.length)
    return variants[idx]
  }

  return {
    overall: pickFortune('overall', 0, 1),
    love: pickFortune('love', 10, 11),
    money: pickFortune('money', 20, 21),
    health: pickFortune('health', 30, 31),
    career: pickFortune('career', 40, 41),
    lucky: {
      color: pick(LUCKY_COLORS, 50),
      number: pick(LUCKY_NUMBERS, 60),
      item: pick(LUCKY_ITEMS, 70),
      time: pick(LUCKY_TIMES, 80),
      direction: pick(LUCKY_DIRECTIONS, 90),
    },
  }
}
