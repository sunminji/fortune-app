function hash(str) {
  let h = 5381
  for (let i = 0; i < str.length; i++) {
    h = ((h << 5) + h) ^ str.charCodeAt(i)
    h = h >>> 0
  }
  return h
}

// 생년월일 + 오늘 기준으로 섞인 카드 덱 반환
export function getShuffledDeck(birthdate) {
  const today = new Date().toISOString().split('T')[0]
  const seed = hash(birthdate + today + 'tarot')
  const deck = [...MAJOR_ARCANA]
  // Fisher-Yates shuffle with seed
  for (let i = deck.length - 1; i > 0; i--) {
    const j = Math.floor(((seed * (i + 1)) % 1000) / 1000 * (i + 1))
    ;[deck[i], deck[j]] = [deck[j], deck[i]]
  }
  // 역방향 여부도 결정
  return deck.map((card, i) => ({
    ...card,
    isReversed: ((hash(String(seed) + i) % 3) === 0), // 33% 확률로 역방향
  }))
}

